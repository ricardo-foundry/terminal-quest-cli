const DUR = '12s';
const W = 820;
const H = 460;

function escape(s) {
  return String(s).replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;');
}

function renderScene(scene, idx) {
  const lines = scene.lines.map((l, i) => {
    const y = 60 + i * 26;
    return `    <text x="20" y="${y}" fill="${l[0]}">${escape(l[1])}</text>`;
  }).join('\n');
  // 4-keyframe envelope: hidden -> hidden -> visible -> hidden
  const k = `${scene.in - 0.001};${scene.in};${scene.out - 0.04};${scene.out}`
    .split(';').map((s) => Math.max(0, Math.min(1, parseFloat(s))).toFixed(4)).join(';');
  return `  <g opacity="0">
    <animate attributeName="opacity" values="0;1;1;0" keyTimes="${k}" dur="${DUR}" repeatCount="indefinite"/>
${lines}
  </g>`;
}

