#!/usr/bin/env node
/**
 * scripts/record-session.js
 *
 * Generate / refresh `docs/terminal-demo.svg` - the animated terminal
 * "demo" used at the top of README.md. We deliberately do NOT shell out
 * to a GIF recorder (no ffmpeg, no asciinema, no node-pty dependency)
 * because the readme has to render on every platform GitHub supports.
 *
 * The output is pure SVG with `<animate>` keyframes, so it works inline
 * on github.com, in NPM's package readme renderer, and as a static OG
 * preview when downscaled.
 *
 * Usage:
 *   node scripts/record-session.js              # rewrite docs/terminal-demo.svg
 *   node scripts/record-session.js --check      # exit 1 if the file is stale
 *
 * The "session" content is defined in `SCENES` below. Edit those four
 * scenes to update the demo.
 */

